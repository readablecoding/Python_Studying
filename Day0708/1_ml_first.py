def cost(W, X, y):
  s = 0
  for i in range(len(X)):
    s +=  (W * X[i] - y[i]) ** 2
    return s / len(X)


def gradient(W, X, y):
  tmp = []
  for i in range(len(X)):
    tmp.append(W * X[i] - y[i])

  s = 0
  for i in range(len(X)):
    s += tmp[i] * X[i]
  
  return s / len(X)



X = [1., 2., 3.]
y = [2., 4., 6.]
W = 100
learning_rate = 0.01


for i in range(1000):
  g = gradient(W, X, y)
  c = cost(W, X, y)
  W = W - g * learning_rate
  
